func sanitizeFilename(s string) string {
	// Replace spaces and special characters with hyphens
	result := ""
	for _, r := range s {
		if (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || (r >= '0' && r <= '9') {
			result += string(r)
		} else if r == ' ' || r == '&' || r == '/' {
			result += "-"
		}
	}
	return result
}
