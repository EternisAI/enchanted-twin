func (c *TDLibClient) WaitForAuthState(ctx context.Context, targetState string, timeout time.Duration) error {
	deadline := time.Now().Add(timeout)
	
	for time.Now().Before(deadline) {
		state, err := c.GetAuthState(ctx)
		if err != nil {
			return err
		}
		
		if state == targetState {
			return nil
		}
		
		select {
		case <-ctx.Done():
			return ctx.Err()
		case <-time.After(1 * time.Second):
		}
	}
	
	return fmt.Errorf("timeout waiting for auth state: %s", targetState)
}

